DROP SCHEMA public CASCADE;

CREATE SCHEMA public;

CREATE TABLE IF NOT EXISTS REVIEWS (
  ID INT NOT NULL AUTO_INCREMENT,
  PROPERTY_ID INT,
  REVIEW VARCHAR(255),
  RATING_ID INT,
  PRIMARY KEY(ID)
);

CREATE TABLE IF NOT EXISTS RATINGS (
  ID INT NOT NULL AUTO_INCREMENT,
  REVIEW_ID INT,
  AVERAGE INT,
  ACCURACY INT,
  COMMUNICATION INT,
  CLEANLINESS INT,
  LOCATION INT,
  CHECKIN INT,
  VALUE INT
  PRIMARY KEY(ID),
  FOREIGN KEY(REVIEW_ID) 
  REFERENCES REVIEWS(ID)
);